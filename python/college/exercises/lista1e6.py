# 6 Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.
raio = float(input('Insira o raio do círculo, cuja área deve ser calculada: '))
print(f'A área do círculo é de {3.1415 * raio ** 2:,.2f} unidades quadradas.')
print(f'Área precisamente: {3.1415 * raio ** 2} unidades quadradas')